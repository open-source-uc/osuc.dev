				sea una experiencia libre de acoso para todo el mundo, independientemente de:
			</p>
			<ul class="list-disc list-inside pl-4 mb-4">
				<li>Edad</li>
				<li>Dimensión corporal</li>
				<li>Discapacidad visible o invisible</li>
				<li>Etnicidad</li>
				<li>Características sexuales</li>
				<li>Identidad y expresión de género</li>
				<li>Nivel de experiencia</li>
				<li>Educación</li>
				<li>Nivel socio-económico</li>
				<li>Nacionalidad</li>
				<li>Apariencia personal</li>
				<li>Raza</li>
				<li>Casta</li>
				<li>Color</li>
				<li>Religión</li>
				<li>Identidad u orientación sexual</li>
			</ul>
			<p>
				Nos comprometemos a actuar e interactuar de maneras que contribuyan a una comunidad abierta,
