				<ul class="list-disc list-inside pl-4">
					<li>Demostrar empatía y amabilidad ante otras personas</li>
					<li>Respeto a diferentes opiniones, puntos de vista y experiencias</li>
					<li>Dar y aceptar adecuadamente retroalimentación constructiva</li>
					<li>
						Aceptar la responsabilidad y disculparse ante quienes se vean afectados por nuestros
						errores, aprendiendo de la experiencia
					</li>
					<li>
						Centrarse en lo que sea mejor no solo para nosotros como individuos, sino para la
						comunidad en general
					</li>
				</ul>
			</div>

			<div>
				<h4 class="text-xl font-semibold mb-2 text-red-600">Comportamientos inaceptables:</h4>
				<ul class="list-disc list-inside pl-4 text-red-800">
					<li>
						El uso de lenguaje o imágenes sexualizadas, y aproximaciones o atenciones sexuales de
						cualquier tipo
					</li>
					<li>
						Comentarios despectivos (trolling), insultantes o derogatorios, y ataques personales o
						políticos
					</li>
					<li>Lenguaje vulgar que pueda dañar la sensibilidad de otros miembros de la comunidad</li>
					<li>El acoso en público o privado</li>
					<li>
						Publicar información privada de otras personas, tales como direcciones físicas o de
						correo electrónico, sin su permiso explícito
					</li>
					<li>
						Otras conductas que puedan ser razonablemente consideradas como inapropiadas en un
						entorno profesional
					</li>
				</ul>
			</div>
		</section>

		<section class="mb-6">
