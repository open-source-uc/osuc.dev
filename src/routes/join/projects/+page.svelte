	<h1>Equipos de Desarrollo</h1>

	<p>
		Cada semestre formamos equipos de trabajo comprometidos a desarrollar proyectos de código
		abierto. Estos proyectos son formados completamente por postulantes y serán apoyados por el
		equipo interno. Trabajamos principalmente en aplicaciones web, pero también consideraremos otras
		tecnologías.
	</p>

	<h2>Que es lo que buscamos</h2>

	<ul>
		<li>Eres motivado y te gusta experimentar y probar soluciones.</li>
		<li>Te gusta trabajar en equipo y aprender de los demás.</li>
		<li>Quieres aprender nuevas tecnologías para resolver nuevos problemas.</li>
		<li>
			Puedes dedicarle al menos <strong>4 horas</strong>
			a la semana para trabajar.
		</li>
	</ul>

	<p>
		Además, te pedimos tener
		<a href="https://telegram.org/?setln=es">Telegram</a>
		y
		<a href="https://github.com">GitHub</a>. Puedes ver
		<a
			href="https://docs.github.com/es/get-started/onboarding/getting-started-with-your-github-account"
			>está guía de GitHub</a
		>
		si es que no tienes cuenta o configurado Git. Cualquier duda puedes preguntar por Telegram y Discord
		(links en la <a href="/">página principal</a>).
	</p>

	<h2>Que es lo que harás</h2>

	<ul>
		<li>
			Participaras en la planificación y el desarrollo de un proyecto de software a lo largo del
			semestre.
		</li>
		<li>
			Tendrás reuniones semanales, alternando entre “stand-ups” para ver como vas y “sprints reviews
			+ planning” para planificar el trabajo de la semana.
		</li>
		<li>
			Trabajaras en torno a requerimientos que serán negociados con tu equipo y con feedback que
			recibirán con usuarios de la aplicación.
		</li>
	</ul>
	<p>
		Buscamos que esta experiencia sea lo más cercana a un trabajo real, para que puedas aprender
		tanto de desarrollo como de trabajo en equipo.
	</p>

	<h2>Qué vas a aprender</h2>
	<ul>
		<li>
			Expandirás tus habilidades técnicas y creativas en torno a un proyecto de software con
			usuarios reales y formato similar a un trabajo real.
		</li>
		<li>
			Aprenderás sobre nuevas tecnologías y herramientas modernas que se utilizan en el desarrollo
			de software.
		</li>
		<li>Practicarás habilidades de trabajo en equipo, comunicación y organización.</li>
	</ul>
	<p>
		Con esto podrás mejorar tus habilidades profesionales y poner en la practica lo que aprendes en
		los cursos. Además podrás participar en talleres, y recibir consejos y feedback de personas con
		experiencia en el área.
	</p>

	<h2>Proceso de postulación</h2>

	<ol>
		<li>
			Al principio de cada semestre, se abrirá un formulario donde te pediremos que respondas
			algunas preguntas, como cuanta experiencia tienes y porqué te gustaría ser parte.
		</li>
		<li>
			Luego tendrás una entrevista para que podamos conocernos mutuamente. No es necesario que
			vengas preparado (no será “un LetCode”), pero te recomendamos que tengas a mano experiencias
			tuyas que nos podrán ayudar a conocerte mejor (como para compartir pantalla).
		</li>
		<li>
			Se realizará un meetup para postulantes donde podrás conocer más sobre nosotros, los proyectos
			que se desarrollaran y las personas que formarán los equipos.
		</li>
	</ol>

